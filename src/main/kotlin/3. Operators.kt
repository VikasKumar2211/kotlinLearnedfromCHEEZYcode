fun main(){
    var i = 13
    var j = 5
    println( i + j)
    println( i + 1)
    println( i - j)
    println( i * j)
    println( i / j)
    println( i.toFloat() / j)
    println( i % j)//Modulus operator %, it give reminder
    println( i > j )
    println( i < j  )
    println( i >= j )
    println( i <= j )
    println( i == j )
    println( i != j )// not equal
}